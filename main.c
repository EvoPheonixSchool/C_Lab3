#include <stdio.h>
#include <stdlib.h>

/**************************************************************************/
/* Defines
 **************************************************************************/
#define SIZE 100

/**************************************************************************/
/* Function prototypes
**************************************************************************/
int toggle_door( char a[ ] );
int doors_state( char a[ ] );


/**************************************************************************
 * Main
 *    
 **************************************************************************/
int main( void ) {
    char array[ SIZE ] = { 0 };
    toggle_door(array);
    doors_state(array);
    return EXIT_SUCCESS;
}

/**************************************************************************
 * Function: 
 **************************************************************************/
int toggle_door(char a[]){
    int c = 1;
    for(int i = 0;i<SIZE;i++){
        if(c == 1){
            for(int p = 0;p<SIZE;p++){
                a[p] = 1;
            }
        }else if(c == 2){
            for(int p = 0;p<SIZE;p++){
                if((p % 2) == 0){
                    a[p] = 0;
                }
            }
        }else if(c == 3){
            for(int p = 0;p<SIZE;p++){
                if((p % 3) == 0){
                    if(a[p] == 0){
                        a[p] = 1;
                    }else{
                        a[p] = 0;
                    }
                }
            }
        }else if(c == 4){
            for(int p = 0;p<SIZE;p++){
                if((p%4) == 0){
                    if(a[p] == 0){
                        a[p] = 1;
                    }else{
                        a[p] = 0;
                    }
                }
            }
        }

        c ++;
        if(c>4){
            c = 1;
        }
    }

    return 0;
}

int doors_state(char a[]){
    for(int i = 0;i<SIZE;i++){
        printf("%i\n",a[i]);
    }
    return 0;
}